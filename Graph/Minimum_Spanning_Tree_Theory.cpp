/* A spanning tree is a sub-graph of an undirected connected graph, which includes all the vertices 
of the graph with a minimum possible number of edges. If a vertex is missed, then it is not a spanning tree. 
The edges may or may not have weights assigned to them. 

A tree in which we have N nodes and N-1 edges and all the nodes are reachable from each other is called spanning tree. */


/* A Minimum Spanning Tree(MST) or minimum weight spanning tree for a weighted, connected, undirected graph 
is a spanning tree having a weight less than or equal to the weight of every other possible spanning tree. 
The weight of a spanning tree is the sum of weights given to each edge of the spanning tree. 
A single graph can have multiple spanning trees. 

Ways to find if a graph is MST :-
1) Prims Algorithm
2) Kruskal's Algorithm */